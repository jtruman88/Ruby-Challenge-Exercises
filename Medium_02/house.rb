class House
  def self.recite
    House.new.assemble_rhyme
  end
  
  def assemble_rhyme
    rhyme = assemble_paragraphs(pieces)
    rhyme.join("\n")
  end
  
  private
  
  def assemble_paragraphs(lines)
    line_one = lines.pop.first + '.'
    beginning = 'This is '
    rhyme = []
    until lines.empty?
      current = "#{beginning}"
      lines.each do |line, line_start|
        current << "#{line}\n#{line_start} "
      end
      current << "#{line_one}\n"
      rhyme.unshift(current)
      lines.shift
    end
    
    rhyme.unshift("#{beginning}#{line_one}\n")
  end
  
  def pieces
    [
